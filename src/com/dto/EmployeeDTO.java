package com.dto;

public class EmployeeDTO {
	
	private String empId;
	private String empName;
	private String empGender;
	private String empGroupId;
	private String empGroupName;
	private String empAddress;
	private String empPhoto;
	private String empSkills;
	private String empSkillsName;
	
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getEmpGroupName() {
		return empGroupName;
	}
	public void setEmpGroupName(String empGroupName) {
		this.empGroupName = empGroupName;
	}
	public String getEmpSkillsName() {
		return empSkillsName;
	}
	public void setEmpSkillsName(String empSkillsName) {
		this.empSkillsName = empSkillsName;
	}
	public String getEmpPhoto() {
		return empPhoto;
	}
	public void setEmpPhoto(String empPhoto) {
		this.empPhoto = empPhoto;
	}
	public String getEmpSkills() {
		return empSkills;
	}
	public void setEmpSkills(String empSkills) {
		this.empSkills = empSkills;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmpGender() {
		return empGender;
	}
	public void setEmpGender(String empGender) {
		this.empGender = empGender;
	}
	public String getEmpGroupId() {
		return empGroupId;
	}
	public void setEmpGroupId(String empGroupId) {
		this.empGroupId = empGroupId;
	}
	public String getEmpAddress() {
		return empAddress;
	}
	public void setEmpAddress(String empAddress) {
		this.empAddress = empAddress;
	}
	public String getPhotoPath() {
		return empPhoto;
	}
	public void setPhotoPath(String photoPath) {
		this.empPhoto = photoPath;
	}
}
